# Generate 500 synthetic samples per class
def generate_synthetic_samples(n=500, label=1):
    noise = np.random.normal(0, 1, (n, latent_dim))
    labels = np.full((n, 1), label)
    generated = generator.predict([noise, labels], verbose=0)
    return generated, labels

X_fake_1, y_fake_1 = generate_synthetic_samples(500, label=1)
X_fake_0, y_fake_0 = generate_synthetic_samples(500, label=0)

# Combine with original data
X_augmented = np.vstack([X_scaled, X_fake_0, X_fake_1])
y_augmented = np.hstack([y_np, y_fake_0.flatten(), y_fake_1.flatten()])
# Reshape for LSTM (samples, timesteps=1, features)
X_lstm = X_augmented.reshape((X_augmented.shape[0], 1, X_augmented.shape[1]))
X_train, X_test, y_train, y_test = train_test_split(X_lstm, y_augmented, test_size=0.2, random_state=42)
class Attention(Layer):
    def build(self, input_shape):
        self.W = self.add_weight(name='att_weight', shape=(input_shape[-1], 1),
                                 initializer='normal', trainable=True)
        self.b = self.add_weight(name='att_bias', shape=(1,),
                                 initializer='zeros', trainable=True)
        super().build(input_shape)
    def call(self, x):
        e = K.tanh(K.dot(x, self.W) + self.b)
        a = K.softmax(e, axis=1)
        return K.sum(x * a, axis=1)

def build_lstm_attention_model(input_shape):
    model = Sequential([
        LSTM(64, return_sequences=True, input_shape=input_shape),
        Dropout(0.3),
        Attention(),
        Dense(32, activation='relu'),
        Dropout(0.3),
        Dense(1, activation='sigmoid')
    ])
    model.compile(loss='binary_crossentropy', optimizer=Adam(0.001), metrics=['accuracy'])
    return model
def evaluate_model(model, X_test, y_test, title="Confusion Matrix"):
    y_pred = model.predict(X_test)
    y_pred = (y_pred > 0.5).astype(int)
    print("Accuracy:", accuracy_score(y_test, y_pred))
    print("\nClassification Report:\n", classification_report(y_test, y_pred))

    cm = confusion_matrix(y_test, y_pred)
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
    plt.xlabel("Predicted")
    plt.ylabel("Actual")
    plt.title(title)
    plt.show()

# Train
model = build_lstm_attention_model((1, X_lstm.shape[2]))
model.fit(X_train, y_train, epochs=50, batch_size=32, validation_data=(X_test, y_test), verbose=1)

# Evaluate
evaluate_model(model, X_test, y_test, title="LSTM + Attention Trained on Real + Synthetic (cGAN) Data")